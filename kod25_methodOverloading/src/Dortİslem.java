public class Dortİslem {
    public int topla(int sayi1, int sayi2){ // dört işlem fonksiyonu tanımladık
        return sayi1+sayi2;
    }


    // diyelim ki programda üç sayıda toplamak istiyoruz kodu tekrar yazmamıza gerek yok
    // bu şekilde yeni 3 sayılı toplama fonksiyonu oluşturduk
    public int topla(int sayi1, int sayi2, int sayi3){
        return sayi1+sayi2+sayi3;
    }


}
